package Week1.Day2;

public class Calculator {

	public int add() {
		int num1 = 2;
		int num2 = 4;
		int num3 = 6;
		int sum;
		sum = num1 + num2 + num3;
		return sum;
	}

	public int sub() {
		int num1 = 27;
		int num2 = 4;

		int sub;
		sub = num1 - num2;
		return sub;
	}

	public double mul() {
		double num1 = 2;
		double num2 = 4;

		double product;
		product = num1 * num2;
		return product;
	}

	public float divide() {
		float num1 = 20;
		float num2 = 4;

		float div;
		div = num1 / num2;
		return div;
	}

}
